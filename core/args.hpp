// C++
#include <vector>
#include <string>

/* ************************************************************************ */

inline namespace core {

/* ************************************************************************ */

/// Application arguments
extern std::vector<std::string> args;

/* ************************************************************************ */

/**
 * @brief Initialize ARGS
 *
 * @param argc
 * @param argv
 */
void init_args(int argc, char** argv) noexcept
{
    args.assign(argv, argv + argc);
}

/* ************************************************************************ */

}

