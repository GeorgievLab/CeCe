
/* ************************************************************************ */

// Declaration
#include "render/Drawable.hpp"

// C++
#include <cassert>

// Simulator
#include "render/errors.hpp"

/* ************************************************************************ */

namespace render {

/* ************************************************************************ */

Drawable::Drawable() noexcept
{
    // Nothing to do
}

/* ************************************************************************ */

Drawable::~Drawable()
{
    if (m_buffer)
        gl(glDeleteBuffers(1, &m_buffer));
}

/* ************************************************************************ */

void Drawable::init()
{
    gl(glGenBuffers(1, &m_buffer));
    assert(m_buffer != 0);
}

/* ************************************************************************ */

}

