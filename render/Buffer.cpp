// Declaration
#include "render/Buffer.hpp"

// C++
#include <cassert>

// Simulator
#include "render/errors.hpp"

/* ************************************************************************ */

namespace render {

/* ************************************************************************ */

Buffer::~Buffer()
{
    if (m_id)
        gl(glDeleteBuffers(1, &m_id));
}

/* ************************************************************************ */

void Buffer::init()
{
    gl(glGenBuffers(1, &m_id));
    assert(m_id != 0);
}

/* ************************************************************************ */

}

